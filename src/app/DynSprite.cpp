#include "DynSprite.h"

namespace app
{
	void DynSprite::updatePos(float dtCoeff, float dt2)
	{
		m_moveVec *= dtCoeff;
		m_moveVec += m_acceleration * dt2;
		m_pos += m_moveVec;
	}
}
